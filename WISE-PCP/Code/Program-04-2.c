#include <stdio.h>
int main() {
    int p = 30;
    int q = 40;
    printf("Value of %d >  %d is %d \n", p, q, p > q);
    printf("Value of %d >= %d is %d \n", p, q, p >= q);
    printf("Value of %d <= %d is %d \n", p, q, p <= q);
    printf("Value of %d <  %d is %d \n", p, q, p < q);
    printf("Value of %d == %d is %d \n", p, q, p == q);
    printf("Value of %d != %d is %d \n", p, q, p != q);
    return 0;
}
